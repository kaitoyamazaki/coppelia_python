% サンプルデータの作成
[X, Y, Z] = peaks;

% 3次元プロットの作成
figure;
%surf(X, Y, Z);
hold on;

% 矢印の描画（回転させるための計算）
x = 0; % 矢印の始点のx座標
y = 0; % 矢印の始点のy座標
z = 0; % 矢印の始点のz座標
u = cosd(45); % 矢印のx方向の長さ（45度回転）
v = sind(45); % 矢印のy方向の長さ（45度回転）
w = 0; % 矢印のz方向の長さ（水平回転なので0）
scale = 2; % スケールファクター

% 矢印を赤色で描画し、スケールを調整
h = quiver3(x, y, z, u, v, w, scale, 'r'); 

% 矢印のヘッドサイズを調整
h.MaxHeadSize = 2; % デフォルトより大きいヘッドサイズ

% 矢印の線の太さを調整
h.LineWidth = 2; % デフォルトより太い線

% 視点を設定（適切な視点に調整）
view(45, 30); % 方位角45度、仰角30度

hold off;